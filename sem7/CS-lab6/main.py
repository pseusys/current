from functools import reduce
from math import fabs


def dic512(original: int, *transformed: int) -> list[float]:
    def set_bits(n: int) -> int:
        return reduce(lambda acc, bit: acc + (1 if bit > 0 else 0), [(n >> k) & 1 for k in range(0, n.bit_length())], 0)
    return [(100 - fabs(0.5 - set_bits(original ^ trs) / 512) * 200) for trs in transformed]


